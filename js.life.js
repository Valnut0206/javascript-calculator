var opr = prompt("Operator:");
var Number1 = prompt("Number1: ");
var Number2 = prompt("Number2: ");

if (opr == "+") {
  prompt(Number1 + Number2);
} else if (opr == "-") {
  prompt(Number1 - Number2);
} else if (opr == "*") {
  prompt(Number1 * Number2);
} else if (opr == "/") {
  prompt(Number1 / Number2);
}
